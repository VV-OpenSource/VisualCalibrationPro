function [k1,k2,k3,k4,b1,b2,b3,b4] = lineEqua(p1,p2,p3,p4)
%求取标定板外围四条线方程
k1 = (p2(2)-p1(2))/(p2(1)-p1(1));  b1 = (p2(1)*p1(2)-p1(1)*p2(2))/(p2(1)-p1(1));
k2 = (p3(2)-p2(2))/(p3(1)-p2(1));  b2 = (p3(1)*p2(2)-p2(1)*p3(2))/(p3(1)-p2(1));
k3 = (p4(2)-p3(2))/(p4(1)-p3(1));  b3 = (p4(1)*p3(2)-p3(1)*p4(2))/(p4(1)-p3(1));
k4 = (p4(2)-p1(2))/(p4(1)-p1(1));  b4 = (p4(1)*p1(2)-p1(1)*p4(2))/(p4(1)-p1(1));
end
